Rect Rect::Intersect( const Rect& a, const Rect& b )
{
	Point LT = Point::Max( a.LT(), b.LT() );
	Point RB = Point::Min( a.RB(), b.RB() );

	if( LT.x <= RB.x && LT.y <= RB.y )
	{
		return Rect( LT, RB );
	}
	else
	{
		return Rect();
	}
}

