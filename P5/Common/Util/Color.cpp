Color Color::Lerp( Color c1, Color c2, float t )
{
	return Color(	P5::Lerp( c1.r, c2.r, t ),
					P5::Lerp( c1.g, c2.g, t ),
					P5::Lerp( c1.b, c2.b, t ),
					P5::Lerp( c1.a, c2.a, t ) );
}

