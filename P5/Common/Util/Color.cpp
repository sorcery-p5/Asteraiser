Color Color::NearTo( Color a, Color aim, Color step )
{
	return Color(	P5::NearTo( a.r, aim.r, step.r ), 
					P5::NearTo( a.g, aim.g, step.g ),
					P5::NearTo( a.b, aim.b, step.b ),
					P5::NearTo( a.a, aim.a, step.a ) );
}

